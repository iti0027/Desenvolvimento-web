// Usamos os operadores de atribuição sempre com o sinal depois o simbolo de igual. exemplo ->
const a = 4
let b = 2
b += a // b = b + a
console.log(b)
b -= 2 // b = b - 2
console.log(b)
b *= 4 // b = b * 4
console.log(b)
b /= 2 // b = b / 2
console.log(b)
b %= 4 // b = b % 4
console.log(b)

// as atribuições vai realizando o novo calculo com o resultado atual da variável